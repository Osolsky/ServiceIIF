    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       this.mdlTable = (DefaultTableModel) jTable1.getModel();
       int seleccion = jTable1.getSelectedRow();
       ID.setText(jTable1.getValueAt(seleccion,0).toString());
       nombreC.setText(jTable1.getValueAt(seleccion,1).toString());
       modeloC.setText(jTable1.getValueAt(seleccion,2).toString());
       filas=seleccion;
    }//GEN-LAST:event_jTable1MouseClicked

    public void modificar() {

        String[] datos = new String[3];
        datos[0] = ID.getText();
        datos[1] = nombreC.getText();
        datos[2] = modeloC.getText();

        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.setValueAt(datos[i], filas, i);
        }
        try {
            String archivo = "Catalogo.txt";
            BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                for (int j = 0; j < jTable1.getColumnCount(); j++) {
                    bw.write((String) (jTable1.getValueAt(i, j)));
                    if (j < jTable1.getColumnCount() - 1) {
                        bw.write(",");
                    }
                }
                bw.newLine();
            }
            bw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        JOptionPane.showMessageDialog(null, "Registro actualizado");
        ID.setText("");
        nombreC.setText("");
        modeloC.setText("");
    }
    
    
    public void eliminar(){
    
     this.mdlTable = (DefaultTableModel) jTable1.getModel();
     mdlTable.removeRow(jTable1.getSelectedRow());
     try{
         String archivo = "Catalogo.txt";
         BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
         for(int i=0;i<jTable1.getRowCount();i++){
             for(int j=0;j<jTable1.getColumnCount();j++){
                 bw.write((String)(jTable1.getValueAt(i,j)));
                 if(j<jTable1.getColumnCount()-1){
                     bw.write("|");
                     
                 }
             }
             bw.newLine();
         }
         bw.close();
     }catch(Exception e){
         JOptionPane.showMessageDialog(null, e);
     }     
     
     JOptionPane.showMessageDialog(null,"Se elimino correctamente");
    }
    
